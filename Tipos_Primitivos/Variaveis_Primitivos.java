package Semana1.Tipos_Primitivos;

public class Variaveis_Primitivos {
    public static void main(String[] args){
        byte n1 = 127;
        short n2 = 32767;
        int n3 = 2147483647;
        long n4 = 9223372036L;
        double n5 =2.77777777;
        float n6 =2.88888f;
        boolean n7 =true;

        System.out.println("byte: " + n1);
        System.out.println("short: " + n2);
        System.out.println("int: " + n3);
        System.out.println("long: " + n4);
        System.out.println("double: " + n5);
        System.out.println("float: " + n6);
        System.out.println("boolean: " + n7);

    }
}
